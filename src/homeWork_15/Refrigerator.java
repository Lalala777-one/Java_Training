package homeWork_15;

public class Refrigerator {
    String model;
    String color;
    String energyConsumption;
    int capacity;
    int noiseLevel;
    int warrantyPeriod;
    String coolingSystem;
    int numberOfShelves;
    String shelfMaterial;

    public Refrigerator(String model, String color, String energyConsumption, String coolingSystem,
                        String shelfMaterial, int capacity, int noiseLevel, int warrantyPeriod, int numberOfShelves) {
        this.model = model;
        this.color = color;
        this.energyConsumption = energyConsumption;
        this.coolingSystem = coolingSystem;
        this.shelfMaterial = shelfMaterial;
        this.capacity = capacity;
        this.noiseLevel = noiseLevel;
        this.warrantyPeriod = warrantyPeriod;
        this.numberOfShelves = numberOfShelves;

    }

}















