package lesson_03;

public class MathOperations {
    public static void main(String[] args) {
        int var1 = 20;
        int var2 = 7;

        int result;

        result = var1 + var2;
        System.out.println("Result is " + result);

        result = result + 5;
        System.out.println("new result is " + result);

        result += 10;
        System.out.println("newnew result is " + result);



        int resultMinus = var1 - var2;

        System.out.println("Result minus = " + resultMinus);


        // умножение

        result = var1 * var2;
        System.out.println("result * is " + result);

        //деление

        result = var1 / var2;
        System.out.println("result / is " + result);







    }
}
